/**

 Pesquisar na lista de valores abaixo, valores inferiores a 7320 
 usando uma das funcoes de pesquisa do array.
 
 A saida do array deve ser ordenada de forma crescente 
 e deve listar todos os valores inferiores a 7320.

Exemplo de saida do array:
1 - 2214
2 - 2244
...

*/
const listaPrecos = [
    3484, 3809, 8612, 188, 7960, 8482, 1202, 6254, 7224, 9992, 8151, 2152, 3491,
    9150, 282, 4842, 4212, 1001, 2728, 9024, 2443, 2214, 2921, 6083, 1729, 1523,
    532, 8156, 7687, 2651
];

let valoresInferiores = listaPrecos.filter(function (preco) {
    return preco < 7320;
});

let valoresInferioresOrdemCrescrente = valoresInferiores.sort((a, b) => a - b);

valoresInferioresOrdemCrescrente.forEach(function (valores, index) {
    console.log(`${++index} - ${valores}`);
});
